package com.te.PhonePay;

public class User {
	String upi_Id;
	String u_name;
	double u_balance;
	long u_mobile_no;
	public String getUpi_Id() {
		return upi_Id;
	}
	public void setUpi_Id(String upi_Id) {
		this.upi_Id = upi_Id;
	}
	public String getU_name() {
		return u_name;
	}
	public void setU_name(String u_name) {
		this.u_name = u_name;
	}
	public double getU_balance() {
		return u_balance;
	}
	public void setU_balance(double u_balance) {
		this.u_balance = u_balance;
	}
	public long getU_mobile_no() {
		return u_mobile_no;
	}
	public void setU_mobile_no(long u_mobile_no) {
		this.u_mobile_no = u_mobile_no;
	}
	
}
